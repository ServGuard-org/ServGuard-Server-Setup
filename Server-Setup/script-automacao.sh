#!/bin/bash

echo '========================================================================================================='
echo 'Iniciando script de automacao' 
echo '========================================================================================================='

sudo apt update && sudo apt upgrade -y

echo '========================================================================================================='
echo 'Iniciando alteracoes de senha'
echo '========================================================================================================='

echo 'Ubuntu:urubu100' | sudo chpasswd
echo 'Root:urubu100' | sudo chpasswd

echo '========================================================================================================='
echo 'Instalando Git'
echo '========================================================================================================='

sudo apt install git -y

echo '========================================================================================================='
echo 'Verificando versao do Git'
git --version || { echo "Erro ao verificar a vers찾o do Git"; exit 1; }
echo '========================================================================================================='

echo '========================================================================================================='
echo 'Instalando Docker'
echo '========================================================================================================='

sudo apt install docker.io -y

echo '========================================================================================================='
echo 'Verificando versao do Docker'
docker --version || { echo "Erro ao verificar a vers찾o do Docker"; exit 1; }
echo '========================================================================================================='

echo '========================================================================================================='
echo 'iniciando e ativando servicos Docker'
echo '========================================================================================================='

sudo systemctl start docker
sudo systemctl enable docker

echo '========================================================================================================='
echo 'instalando docker compose'
echo '========================================================================================================='

sudo curl -L "https://github.com/docker/compose/releases/latest/download/docker-compose-$(uname -s)-$(uname -m)" -o /usr/local/bin/docker-compose
sudo chmod +x /usr/local/bin/docker-compose

echo '========================================================================================================='
echo 'verificando se foi instalado'
echo '========================================================================================================='

docker-compose --version || { echo "Erro ao verificar a vers찾o do Docker Compose"; exit 1; }

echo '========================================================================================================='
echo 'Clonando repositorio no github: ServGuard'
echo '========================================================================================================='

git clone https://github.com/ServGuard-org/ServGuard-WebSite.git
git clone https://github.com/ServGuard-org/ServGuard-Setups.git

echo '========================================================================================================='
echo 'Criando novo e mudando de diretorio'
echo '========================================================================================================='

cd /home/ubuntu/ServGuard-WebSite || { echo "Erro ao mudar de diret처rio"; exit 1; }

echo '========================================================================================================='
echo 'Rodando docker-compose'
echo '========================================================================================================='

docker-compose -f docker-compose-ee.yml up -d || { echo "Erro ao executar docker-compose"; exit 1; }

echo '========================================================================================================='
echo 'Listando as imagens'
echo '========================================================================================================='

docker images

echo '========================================================================================================='
echo 'Verificando se os containers foram criados'
echo '========================================================================================================='

docker ps -a
